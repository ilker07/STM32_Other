void ST7789_WriteChar(uint8_t x, uint8_t y, char ch, FontDef font, uint16_t color, uint16_t bgcolor)
{
	uint32_t i, b, j;
	
	ST7789_SetAddressWindow(x, y, x + font.width - 1, y + font.height - 1);

	for (i = 0; i < font.height; i++) 
	{
			b = font.data[(ch - 32) * font.height + i];
				for (j = 0; j < font.width; j++) 
				{
							if ((b << j) & 0x8000) 
								{
								
								ST7789_WriteSmallData(color >> 8);
								ST7789_WriteSmallData(color & 0xFF);
							  }
							else 
								{
								
									ST7789_WriteSmallData(bgcolor >> 8);
								  ST7789_WriteSmallData(bgcolor & 0xFF);
							  }
				}
	}
	
}


 void ST7789_WriteSmallData(uint8_t data)
{
	
	
	GPIOA->BSRR |=1<<20; //A4 RESET
	
	GPIOA->BSRR |=1<<0; //dc 1 
	//SPI1->CR1 |= 1<<6;
	
	
   SPI1->DR=data;
   while(!(SPI1->SR & 0x0002));
	 while((SPI1->SR & 0x0080));  //BSY =0 olmasini bekle.
	uint32_t temp=0x00U;
	temp=SPI1->DR;
	temp=SPI1->SR;
	GPIOA->BSRR |=1<<4;//A4 SET
	(void)temp;
	
}


void ST7789_SetRotation(uint8_t m)
{
	ST7789_WriteCommand(ST7789_MADCTL);	// MADCTL

	switch (m) {
	case 0:
		ST7789_WriteSmallData(ST7789_MADCTL_MX | ST7789_MADCTL_MY | ST7789_MADCTL_RGB);
		break;
	case 1:
		ST7789_WriteSmallData(ST7789_MADCTL_MY | ST7789_MADCTL_MV | ST7789_MADCTL_RGB);
		break;
	case 2:
		ST7789_WriteSmallData(ST7789_MADCTL_RGB);
		break;
	case 3:
		ST7789_WriteSmallData(ST7789_MADCTL_MX | ST7789_MADCTL_MV | ST7789_MADCTL_RGB);
		break;
	default:
		break;
	}
}


